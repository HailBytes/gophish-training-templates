#!/usr/bin/env python3
"""
Per-Category README Generator

Reads every category metadata.json and writes a README.md into that category
folder describing its purpose, the templates it contains (with attack vector,
difficulty, estimated click rate, suggested subject lines, gophish variables,
the paired training page, and operator notes), and how to use them.

Output is deterministic (no wall-clock date is embedded) so the generated files
can be committed and checked for staleness in CI.

Usage:
    python3 tools/generate_folder_readmes.py            # write every <category>/README.md
    python3 tools/generate_folder_readmes.py --check     # exit 1 if any README is stale
    python3 tools/generate_folder_readmes.py --stdout     # print all to stdout, don't write
"""

import sys
import json
import argparse
from pathlib import Path

ROOT = Path(__file__).parent.parent

# Top-level directories that are not template categories (no metadata.json).
SKIP_DIRS = {".git", "tools", "landing-pages", "campaign-guides", "docs", "tests"}

AUTOGEN = (
    "<!-- AUTOGENERATED by tools/generate_folder_readmes.py — do not edit by hand. -->\n"
    "<!-- Run `python3 tools/generate_folder_readmes.py` to regenerate. -->\n"
)


def load_categories():
    cats = []
    for metadata_path in sorted(ROOT.glob("*/metadata.json")):
        if metadata_path.parent.name in SKIP_DIRS:
            continue
        try:
            meta = json.loads(metadata_path.read_text())
        except json.JSONDecodeError as e:
            print(f"WARNING: skipping invalid {metadata_path}: {e}", file=sys.stderr)
            continue
        cats.append((metadata_path.parent.name, meta))
    return cats


def render(dir_name, meta) -> str:
    templates = meta.get("templates", [])
    title = meta.get("category", dir_name)
    industry = meta.get("industry", "")
    description = meta.get("description", "")
    lang = meta.get("language")

    L = [AUTOGEN]
    L.append(f"# `{dir_name}` — {title} templates\n")

    bits = []
    if industry:
        bits.append(f"**Industry:** {industry}")
    if lang:
        bits.append(f"**Language:** {lang}")
    bits.append(f"**Templates:** {len(templates)}")
    L.append("  ·  ".join(bits) + "\n")

    if description:
        L.append(description + "\n")

    L.append(
        "Phishing-simulation templates for **authorized** security awareness "
        "training with [GoPhish](https://getgophish.com/) and "
        "[HailBytes SAT](https://hailbytes.com/sat?utm_source=github&utm_medium=folder_readme&utm_campaign=gophish-training-templates). "
        "See the [repository README](../README.md) for setup and the "
        "[full template catalog](../docs/CATALOG.md) for every category at a glance.\n"
    )

    # Summary table
    L.append("## Templates\n")
    L.append("| Template | File | Attack vector | Difficulty | Est. click rate |")
    L.append("|---|---|---|---|---|")
    for t in templates:
        L.append(
            f"| {t.get('name','—')} "
            f"| [`{t.get('filename','—')}`]({t.get('filename','')}) "
            f"| {t.get('attack_vector','—')} "
            f"| {t.get('difficulty','—')} "
            f"| {t.get('estimated_click_rate','—')} |"
        )
    L.append("")

    # Per-template detail
    L.append("## Template details\n")
    for t in templates:
        L.append(f"### {t.get('name','—')}\n")
        L.append(f"- **File:** [`{t.get('filename','—')}`]({t.get('filename','')})")
        meta_line = (
            f"- **Attack vector:** {t.get('attack_vector','—')} · "
            f"**Difficulty:** {t.get('difficulty','—')} · "
            f"**Est. click rate:** {t.get('estimated_click_rate','—')}"
        )
        L.append(meta_line)
        gvars = t.get("gophish_variables", [])
        if gvars:
            L.append("- **GoPhish variables:** " + " ".join(f"`{v}`" for v in gvars))
        edu = t.get("education_page")
        if edu:
            L.append(f"- **Paired training page:** [`{edu}`]({edu})")
        subjects = t.get("suggested_subject_lines", [])
        if subjects:
            L.append("- **Suggested subject lines:**")
            for s in subjects:
                L.append(f"  - {s}")
        notes = t.get("notes")
        if notes:
            L.append(f"- **Operator notes:** {notes}")
        L.append("")

    # How to use
    L.append("## How to use\n")
    L.append(
        "1. In GoPhish, go to **Email Templates → New Template** and paste the HTML "
        "from the file above. Set a subject line from the suggestions.\n"
        "2. Create a **Landing Page** (see [`../landing-pages/`](../landing-pages/)) "
        "to capture the click, and — where a training page is listed — redirect "
        "clickers to it so they get immediate education.\n"
        "3. Configure a realistic **Sending Profile** and launch to a consented, "
        "in-scope group. Schedule during business hours for realism.\n"
    )
    L.append(
        "> **Before sending:** customize sender names, domains, logos, and any brand "
        "impersonations to match your engagement and your authorization scope. "
        "These templates are for authorized training only.\n"
    )

    L.append("---")
    L.append(
        "_Part of [gophish-training-templates](../README.md). "
        "This file is generated — run `python3 tools/generate_folder_readmes.py` to update it._"
    )
    return "\n".join(L).rstrip() + "\n"


def main():
    ap = argparse.ArgumentParser(description="Generate per-category README files")
    ap.add_argument("--check", action="store_true",
                    help="Exit non-zero if any committed README is out of date")
    ap.add_argument("--stdout", action="store_true",
                    help="Print all generated content to stdout instead of writing")
    args = ap.parse_args()

    cats = load_categories()
    stale = []
    for dir_name, meta in cats:
        content = render(dir_name, meta)
        out = ROOT / dir_name / "README.md"
        if args.stdout:
            sys.stdout.write(f"\n===== {out.relative_to(ROOT)} =====\n")
            sys.stdout.write(content)
            continue
        if args.check:
            existing = out.read_text() if out.exists() else ""
            if existing != content:
                stale.append(str(out.relative_to(ROOT)))
            continue
        out.write_text(content)

    if args.stdout:
        return
    if args.check:
        if stale:
            print("✗ Stale category READMEs:\n  " + "\n  ".join(stale), file=sys.stderr)
            print("Run `python3 tools/generate_folder_readmes.py` and commit.", file=sys.stderr)
            sys.exit(1)
        print(f"✓ All {len(cats)} category READMEs are up to date.")
        return
    print(f"✓ Wrote README.md for {len(cats)} categories.")


if __name__ == "__main__":
    main()
