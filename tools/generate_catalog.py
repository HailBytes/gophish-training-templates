#!/usr/bin/env python3
"""
Template Catalog Generator

Reads every category metadata.json and renders a single Markdown catalog of all
templates, grouped by category. The output is deterministic (no wall-clock date
is embedded — the "last updated" value is derived from the metadata itself), so
the generated file can be committed and checked for staleness in CI.

Usage:
    python3 tools/generate_catalog.py            # write docs/CATALOG.md
    python3 tools/generate_catalog.py --check     # exit 1 if docs/CATALOG.md is stale
    python3 tools/generate_catalog.py --stdout    # print to stdout, don't write
"""

import sys
import json
import argparse
from pathlib import Path

ROOT = Path(__file__).parent.parent
OUTPUT = ROOT / "docs" / "CATALOG.md"

# Top-level directories that are not template categories. (The catalog only
# globs top-level */metadata.json, so per-category education/ subdirs are never
# matched — but the top-level education/ dir IS a real category and is kept.)
SKIP_DIRS = {".git", "tools", "landing-pages", "campaign-guides", "docs"}

HEADER = """<!-- AUTOGENERATED by tools/generate_catalog.py — do not edit by hand. -->
<!-- Run `python3 tools/generate_catalog.py` to regenerate. -->

# Template Catalog

A complete, auto-generated index of every phishing simulation template in this
repository, grouped by category. Regenerate with `python3 tools/generate_catalog.py`.

All templates are ready to deploy in self-hosted [GoPhish](https://getgophish.com/)
and are fully compatible with **[HailBytes SAT](https://hailbytes.com/sat?utm_source=github&utm_medium=repo_catalog&utm_campaign=gophish-training-templates)**,
the enterprise-ready successor to GoPhish, which imports this metadata directly.
"""


def load_categories():
    """Return a sorted list of (dir_name, metadata dict) for every category."""
    categories = []
    for metadata_path in sorted(ROOT.glob("*/metadata.json")):
        if metadata_path.parent.name in SKIP_DIRS:
            continue
        try:
            metadata = json.loads(metadata_path.read_text())
        except json.JSONDecodeError as e:
            print(f"WARNING: skipping invalid {metadata_path}: {e}", file=sys.stderr)
            continue
        categories.append((metadata_path.parent.name, metadata))
    return categories


def render(categories) -> str:
    total_templates = sum(len(m.get("templates", [])) for _, m in categories)
    last_updated = max(
        (str(m.get("last_updated", "")) for _, m in categories), default=""
    )

    lines = [HEADER]
    lines.append("## Summary\n")
    lines.append(f"- **Templates:** {total_templates}")
    lines.append(f"- **Categories:** {len(categories)}")
    if last_updated:
        lines.append(f"- **Most recent update:** {last_updated}")
    lines.append("")

    for dir_name, meta in categories:
        templates = meta.get("templates", [])
        title = meta.get("category", dir_name)
        industry = meta.get("industry", "")
        description = meta.get("description", "")
        lang = meta.get("language")

        heading = f"### `{dir_name}` — {title}"
        if lang:
            heading += f" ({lang})"
        lines.append(heading)
        lines.append("")
        meta_bits = []
        if industry:
            meta_bits.append(f"**Industry:** {industry}")
        meta_bits.append(f"**Templates:** {len(templates)}")
        lines.append("  ·  ".join(meta_bits))
        lines.append("")
        if description:
            lines.append(f"_{description}_")
            lines.append("")

        lines.append("| Template | File | Vector | Difficulty | Est. Click Rate | Subject Lines |")
        lines.append("|---|---|---|---|---|---|")
        for t in templates:
            name = t.get("name", "—")
            filename = t.get("filename", "—")
            vector = t.get("attack_vector", "—")
            difficulty = t.get("difficulty", "—")
            rate = t.get("estimated_click_rate", "—")
            n_subjects = len(t.get("suggested_subject_lines", []))
            file_link = f"[`{filename}`]({dir_name}/{filename})"
            lines.append(
                f"| {name} | {file_link} | {vector} | {difficulty} | {rate} | {n_subjects} |"
            )
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def main():
    parser = argparse.ArgumentParser(description="Generate the template catalog")
    parser.add_argument("--check", action="store_true",
                        help="Exit non-zero if the committed catalog is out of date")
    parser.add_argument("--stdout", action="store_true",
                        help="Print to stdout instead of writing the file")
    args = parser.parse_args()

    categories = load_categories()
    content = render(categories)

    if args.stdout:
        sys.stdout.write(content)
        return

    if args.check:
        existing = OUTPUT.read_text() if OUTPUT.exists() else ""
        if existing != content:
            print(
                f"✗ {OUTPUT.relative_to(ROOT)} is out of date. "
                f"Run `python3 tools/generate_catalog.py` and commit the result.",
                file=sys.stderr,
            )
            sys.exit(1)
        print(f"✓ {OUTPUT.relative_to(ROOT)} is up to date.")
        return

    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT.write_text(content)
    print(f"✓ Wrote {OUTPUT.relative_to(ROOT)} "
          f"({sum(len(m.get('templates', [])) for _, m in categories)} templates, "
          f"{len(categories)} categories).")


if __name__ == "__main__":
    main()
